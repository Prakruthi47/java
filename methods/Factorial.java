class Factorial {

	public static int toGetFact(int factNumber) {
		//System.out.println("method started");
		int fact =1;
		for(int num =1;num<=factNumber;num++ ){
	
			fact = num * fact;
			//System.out.println("the factorial of " +FactNumber+" is "+fact);	
	
		}
		//System.out.println("the factorial of " +factNumber+" is "+fact);
		//System.out.println("method ended");
		return fact;






}







}